import numpy as np
#zeroes array
array = np.zeros((3,4))
print(array)
#zeroes array
print("---------------")
array2 = np.zeros((3,4),dtype=int)
print(array2)
#ones array
print("---------------")
array3 = np.ones((3,5,4))
print(array3)
#ones array
print("---------------")
array3 = np.ones((3,5,4),dtype=int)
print(array3)
#range array
print("----------------")
array4 = np.arange(200,600,10)
print(array4)
print("----------------")
array5 = np.eye(5)
print(array5)
print("----------------")
array6 = np.eye(5,dtype=int)
print(array6)

