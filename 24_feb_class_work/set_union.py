# Program to perform union of two sets

set1 = {1, 2, 3, 4}                 # Create first set
set2 = {3, 4, 5, 6}                 # Create second set

result = set1.union(set2)           # Perform union operation

print("Union of sets:", result)     # Display union result

# Output Example
# Union of sets: {1, 2, 3, 4, 5, 6}
