# Program to find the sum of elements in a list

numbers = [10, 20, 30, 40]               # Create a list of numbers

total = 0                                # Initialize sum variable

for num in numbers:                      # Loop through each element
    total = total + num                  # Add element to total sum

print("Sum of list elements:", total)    # Display total sum

# Output Example
# Sum of list elements: 100
