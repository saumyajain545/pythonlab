# Program to check whether one set is subset of another

set1 = {1, 2}                       # Create first set
set2 = {1, 2, 3, 4}                 # Create second set

if set1.issubset(set2):             # Check if set1 is subset of set2
    print("set1 is subset of set2") # Display subset result
else:                               # Otherwise
    print("set1 is not subset of set2")

# Output Example
# set1 is subset of set2
