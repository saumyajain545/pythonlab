# Program to perform difference of two sets

set1 = {1, 2, 3, 4}                 # Create first set
set2 = {3, 4, 5, 6}                 # Create second set

result = set1.difference(set2)      # Find elements present in set1 but not in set2

print("Difference of sets:", result) # Display difference result

# Output Example
# Difference of sets: {1, 2}
