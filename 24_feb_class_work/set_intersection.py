# Program to perform intersection of two sets

set1 = {1, 2, 3, 4}                 # Create first set
set2 = {3, 4, 5, 6}                 # Create second set

result = set1.intersection(set2)    # Perform intersection operation

print("Intersection of sets:", result)  # Display intersection result

# Output Example
# Intersection of sets: {3, 4}
