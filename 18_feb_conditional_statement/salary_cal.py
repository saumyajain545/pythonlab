# Given values
basic_salary = 25000
hra_percentage = 20
bonus = 5000

# Calculate HRA
hra = basic_salary * hra_percentage / 100

# Calculate total monthly salary
total_salary = basic_salary + hra + bonus

# Display results
print("Basic Salary: ₹", basic_salary)
print("HRA (20% of Basic): ₹", hra)
print("Bonus: ₹", bonus)
print("Total Monthly Salary: ₹", total_salary)
